def verdopple(zahl):
    """
    Verdoppelt eine gegebene Zahl und gibt das Ergebnis zurück.

    Parameter:
        zahl (int oder float): Die Zahl, die verdoppelt werden soll.

    Rückgabewert:
        int oder float: Das Doppelte der Eingabe.
    """
    return zahl * 2  # Multipliziert die Eingabe mit 2 und speichert das Ergebnis in "ergebnis"